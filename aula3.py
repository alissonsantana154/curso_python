"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Álisson santana')
print(1, 'Álisson "santana"')


# Aspas Duplas 
print("Álisson santana")
print(2, "Álisson 'santana'")

# Escape
print("Álisson \"santana\"")

# r 
print(r"Álisson \"santana\"")

